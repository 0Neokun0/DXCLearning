package Java_Basics;
public class VariableTypes {
    public static void main(String args[]) {
        // 数字の変数
        int a;
        a = 26;
        System.out.println(a);

        // 文字の変数
        String b;
        b = "おはようございます！";
        System.out.println(b);

        // 計算
        int x;
        int x1;
        int x2;
        x1 = 10;
        x2 = 5;
        x = x1 + x2;
        System.out.println(x); // 足し算
        x = x1 / x2;
        System.out.println(x); // 割り算
        x = x1 * x2;
        System.out.println(x); // 掛け算
        x = x1 - x2;
        System.out.println(x); // 引き算
    }
}
