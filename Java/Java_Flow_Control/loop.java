public class loop {
    public static void main(String[] args) {

        int n = 5;
        // ループ
        for (int i = 1; i <= n; ++i) {
            System.out.println("Java 面白い");
        }
        System.out.println("---------------------------------");

        int j = 1, m = 5;
        // While ループ1から５
        while (j <= m) {
            System.out.println(j);
            j++;
        }
    }
}
